    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        try {
            Connection conn = null;
            DBUtil.createDataSource("localhost", "industrial", txtUsuario.getText(), new String(txtClave.getPassword()));
            conn = DBUtil.crearConexion();
            boolean ok = conn != null;
            DBUtil.cerrar(conn);
            if (ok) {
                Menu menu = new Menu();
                menu.setVisible(true);
                setVisible(false);
            } else {
                JOptionPane.showMessageDialog(this, "No se puede autenticar", "Errores de autenticaci칩n", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "No se puede autenticar", "Errores de autenticaci칩n", JOptionPane.WARNING_MESSAGE);
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_btnOkActionPerformed

